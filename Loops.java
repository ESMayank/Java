public class Loops {
    public static void main(String[] args) {
        int i = 1;
        while(i<=3){
            System.out.println(i);
            i++;
        }
        for (int j = 0;j<=10;j++){
            System.out.println(j);
        }
        int z = 5;
        do{
            System.out.println(z);
            z++;
        }while(z==0);
    }
}
